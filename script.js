const languageButton = document.getElementById("langToggle");
const currentLanguageLabel = document.querySelector(".lang-current");
const alternateLanguageLabel = document.querySelector(".lang-alt");
const navLinks = Array.from(document.querySelectorAll(".nav-links a"));
const sections = Array.from(document.querySelectorAll("main section[id]"));

function getNestedValue(source, path) {
  return path.split(".").reduce((value, key) => value?.[key], source);
}

function applyLanguage(lang) {
  const dictionary = copy[lang] || copy.en;
  document.documentElement.lang = lang === "zh" ? "zh-CN" : "en";
  document.title = lang === "zh"
    ? "Xianwei Guo | AI 应用与 Agent 工程"
    : "Xianwei Guo | AI Application & Agent Engineer";

  document.querySelectorAll("[data-i18n]").forEach((node) => {
    const value = getNestedValue(dictionary, node.dataset.i18n);
    if (value) node.textContent = value;
  });

  currentLanguageLabel.textContent = lang === "zh" ? "ZH" : "EN";
  alternateLanguageLabel.textContent = lang === "zh" ? "EN" : "ZH";
  localStorage.setItem("preferred-language", lang);
}

function hydrateLanguage() {
  const saved = localStorage.getItem("preferred-language");
  applyLanguage(saved === "zh" ? "zh" : "en");
}

function setupLanguageToggle() {
  languageButton.addEventListener("click", () => {
    const next = document.documentElement.lang === "zh-CN" ? "en" : "zh";
    applyLanguage(next);
  });
}

function setupReveal() {
  const targets = document.querySelectorAll(".reveal");
  const observer = new IntersectionObserver((entries) => {
    entries.forEach((entry) => {
      if (!entry.isIntersecting) return;
      entry.target.classList.add("visible");
      observer.unobserve(entry.target);
    });
  }, { threshold: 0.12 });

  targets.forEach((target) => observer.observe(target));
}

function setupActiveNavigation() {
  const observer = new IntersectionObserver((entries) => {
    entries.forEach((entry) => {
      if (!entry.isIntersecting) return;
      navLinks.forEach((link) => {
        link.classList.toggle("active", link.getAttribute("href") === `#${entry.target.id}`);
      });
    });
  }, { rootMargin: "-35% 0px -55% 0px" });

  sections.forEach((section) => observer.observe(section));
}

hydrateLanguage();
setupLanguageToggle();
setupReveal();
setupActiveNavigation();
